// ===================== 侧边栏交互 =====================
function bindSidebarEvents() {
    // 判空避免报错
    if (!sidebar || !overlay || !sidebarToggle || !closeBtn) return;

    // 移动端打开侧边栏
    sidebarToggle.addEventListener('click', () => {
        sidebar.classList.add('open');
        overlay.classList.add('show');
    });

    // 关闭侧边栏
    closeBtn.addEventListener('click', closeSidebar);
    overlay.addEventListener('click', closeSidebar);

    // 窗口大小变化适配
    window.addEventListener('resize', () => {
        if (window.innerWidth > 768) {
            sidebar.classList.remove('open');
            overlay.classList.remove('show');
        }
    });
}

function closeSidebar() {
    if (sidebar && overlay) {
        sidebar.classList.remove('open');
        overlay.classList.remove('show');
    }
}

// ===================== 章节切换 =====================
function initChapterSwitch() {
    const chapterBtns = document.querySelectorAll('.chapter-btn');
    if (!chapterBtns.length) return;

    // 绑定按钮点击事件
    chapterBtns.forEach(btn => {
        btn.addEventListener('click', function () {
            currentYear = parseInt(this.dataset.year) || 2023; // 容错：默认2023
            updateTimeline(currentYear);
            // 更新按钮选中状态
            chapterBtns.forEach(b => b.classList.remove('active'));
            this.classList.add('active');
            // 更新内容
            updateChapterContent(this.dataset.chapter);
        });
    });

    // 默认激活第一个按钮
    chapterBtns[0].classList.add('active');
    updateChapterContent(chapterBtns[0].dataset.chapter);
}

// 更新章节内容（正文+事件）
function updateChapterContent(chapterKey) {
    if (!chapterContent || !eventsContainer) return; // 判空

    const data = chapterContentMap[chapterKey] || chapterContentMap["第一"];

    // 内容卡片渲染（原有逻辑+图片多图适配）
    let contentHTML = `<h2><span>${data.title.split('：')[0]}✨</span> ${data.title.split('：')[1]}</h2>`;
    data.content.forEach(text => {
        contentHTML += `<p>${text}</p>`;
    });
    chapterContent.innerHTML = contentHTML;

    // 事件列表渲染（支持多图）
    let eventsHTML = '';
    data.events.forEach((event, index) => {
        const isOpen = index === 0 ? 'show' : '';
        const btnText = index === 0 ? '收起详情 ▲' : '查看详情 ▼';

        let eventDescHTML = '';
        // 解析图片配置
        event.desc.forEach(descItem => {
            // 多图配置
            if (descItem.includes('<!-- imgs: ')) {
                try {
                    const imgsConfigStr = descItem.replace(/<!-- imgs: | -->/g, '');
                    const imgsConfig = JSON.parse(imgsConfigStr);
                    eventDescHTML += `<div class="event-img-wrapper">`;
                    imgsConfig.forEach(img => {
                        eventDescHTML += `<img src="${img.src}" alt="${img.alt}" class="event-img" onerror="this.src='img/default.jpg'">`; // 增加图片容错
                    });
                    eventDescHTML += `</div>`;
                } catch (e) {
                    console.warn('多图配置解析失败：', e);
                    eventDescHTML += descItem; // 解析失败则显示原文本
                }
            }
            // 单图配置
            else if (descItem.includes('<!-- img: ')) {
                try {
                    const imgConfigStr = descItem.replace(/<!-- img: | -->/g, '');
                    const imgConfig = JSON.parse(imgConfigStr);
                    eventDescHTML += `
                        <div class="event-img-wrapper">
                            <img src="${imgConfig.src}" alt="${imgConfig.alt}" class="event-img" onerror="this.src='img/default.jpg'">
                        </div>
                    `;
                } catch (e) {
                    console.warn('单图配置解析失败：', e);
                    eventDescHTML += descItem; // 解析失败则显示原文本
                }
            }
            // 普通文本
            else {
                eventDescHTML += descItem;
            }
        });

        eventsHTML += `
            <div class="event-item">
                <div class="event-header" onclick="toggleEvent(this)">
                    <div class="event-title">
                        <div class="event-icon">${event.icon}</div>
                        <span>${event.title}</span>
                    </div>
                    <div class="expand-btn">${btnText}</div>
                </div>
                <div class="event-content ${isOpen}">
                    <div class="event-desc">
                        ${eventDescHTML}
                    </div>
                </div>
            </div>
        `;
    });
    eventsContainer.innerHTML = eventsHTML;
}

// ===================== 时间轴 =====================
function updateTimeline(year) {
    const yearElements = document.querySelectorAll('.timeline-years span');
    if (!yearElements.length) return;

    // 更新年份的active状态
    yearElements.forEach(el => {
        el.classList.remove('active');
        if (parseInt(el.dataset.year) === year) {
            el.classList.add('active');
        }
    });
}

// ===================== 事件展开/收起 =====================
function toggleEvent(header) {
    const content = header.nextElementSibling;
    const btn = header.querySelector('.expand-btn');
    if (!content || !btn) return;

    // 优化：添加过渡动画（需配合CSS：.event-content { transition: all 0.3s; }）
    content.classList.toggle('show');
    btn.textContent = content.classList.contains('show') ? '收起详情 ▲' : '查看详情 ▼';
    // 修复：opacity+height过渡更自然
    content.style.opacity = content.classList.contains('show') ? 1 : 0;
    content.style.maxHeight = content.classList.contains('show') ? '2000px' : '0';
}

// ===================== 弹幕功能（核心修复：按钮表情包不消失） =====================
function createDanmaku() {
    if (!isDanmakuOpen || !danmakuContainer) return;

    // 随机选弹幕文本
    const text = danmakuList[Math.floor(Math.random() * danmakuList.length)];
    // 随机高度（避免堆叠，限定在容器内）
    const containerHeight = danmakuContainer.offsetHeight;
    const top = Math.random() * (containerHeight - 40); // 40是弹幕高度，避免超出
    // 随机动画时长（8-15秒，避免速度一致）
    const duration = 8 + Math.random() * 7;
    // 随机颜色（青春系配色，保证可读性）
    const colors = ['#ff7e7e', '#7e9fff', '#7eff9f', '#ffd77e', '#ff7ef5'];
    const color = colors[Math.floor(Math.random() * colors.length)];

    // 创建弹幕元素
    const danmaku = document.createElement('div');
    danmaku.className = 'danmaku-item';
    danmaku.textContent = text;
    danmaku.style.top = `${top}px`;
    danmaku.style.color = color;
    danmaku.style.right = '0px'; // 强制初始在最右端
    // 绑定动画（指定动画名称+时长）
    danmaku.style.animation = `danmaku-scroll ${duration}s linear forwards`;

    // 加入容器 + 动画结束后自动删除（避免内存泄漏）
    danmakuContainer.appendChild(danmaku);
    danmaku.addEventListener('animationend', () => {
        danmaku.remove();
    });
}

// 启动弹幕（确保只创建一个定时器）
function startDanmaku() {
    // 先清除旧定时器（核心！避免重复）
    if (danmakuInterval) clearInterval(danmakuInterval);
    // 立即生成1条，然后每隔1.5秒生成1条（避免刷屏）
    createDanmaku();
    danmakuInterval = setInterval(createDanmaku, 1500);
}

// 停止弹幕（彻底清除定时器+清空容器）
function stopDanmaku() {
    clearInterval(danmakuInterval);
    danmakuInterval = null; // 重置定时器
    if (danmakuContainer) danmakuContainer.innerHTML = '';
}

// 切换弹幕开关（核心修复：保留表情包/图标，用innerHTML而非textContent）
function toggleDanmaku() {
    if (!danmakuToggleBtn) return;

    isDanmakuOpen = !isDanmakuOpen;
    if (isDanmakuOpen) {
        // 开启：更新按钮样式 + 保留表情包 + 启动弹幕
        danmakuToggleBtn.classList.remove('off');
        danmakuToggleBtn.innerHTML = '😜 关闭弹幕'; // 用innerHTML保留表情包
        startDanmaku();
    } else {
        // 关闭：更新按钮样式 + 保留表情包 + 停止弹幕 + 清空容器
        danmakuToggleBtn.classList.add('off');
        danmakuToggleBtn.innerHTML = '😯 开启弹幕'; // 用innerHTML保留表情包
        stopDanmaku();
    }
}

// 初始化弹幕（整合逻辑，只执行一次）
function initDanmaku() {
    if (!danmakuToggleBtn || !danmakuContainer) return;

    // 初始化按钮文本（默认带表情包）
    danmakuToggleBtn.innerHTML = '😜 关闭弹幕';
    // 绑定开关按钮事件（仅绑定一次）
    danmakuToggleBtn.addEventListener('click', toggleDanmaku);
    // 初始启动弹幕
    if (isDanmakuOpen) {
        // 微任务确保容器渲染完成后启动
        setTimeout(() => startDanmaku(), 0);
    }
}

